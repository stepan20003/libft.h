#include "libft.h"

char	*ft_strdup(const char *s)
{
	int		size;
	int		i;
	char	*p;

	i = 0;
	size = ft_strlen(s)+1;
	p = (char *)malloc(size);
	if (p == NULL)
		return (NULL);
	while (s[i])
	{
		p[i] = s[i];
		i++;
	}
	p[i] = '\0';
	return (p);
}
